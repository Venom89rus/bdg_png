# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –±–∏–±–ª–∏–æ—Ç–µ–∫–∏:
import streamlit as st  # –æ—Å–Ω–æ–≤–Ω–æ–π –º–æ–¥—É–ª—å –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞ Streamlit
import pandas as pd     # –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å —Ç–∞–±–ª–∏—Ü–∞–º–∏ –∏ Excel-—Ñ–∞–π–ª–∞–º–∏
import matplotlib.pyplot as plt  # –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è –ø–æ—Å—Ç—Ä–æ–µ–Ω–∏—è –≥—Ä–∞—Ñ–∏–∫–æ–≤ (–∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –≤ –¥–æ—á–µ—Ä–Ω–∏—Ö –º–æ–¥—É–ª—è—Ö)

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏–∏ –∞–Ω–∞–ª–∏–∑–∞ –∏–∑ –æ—Ç–¥–µ–ª—å–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤ (–º–æ–¥—É–ª–µ–π):
from ch4_analysis import run as run_ch4     # –ê–Ω–∞–ª–∏–∑ –°–ù‚ÇÑ
from c3plus_calc import run as run_c3       # –ê–Ω–∞–ª–∏–∑ –°‚ÇÉ+–≤
from c5plus_calc import run as run_c5       # –ê–Ω–∞–ª–∏–∑ –°‚ÇÖ+–≤

# –ì–ª–∞–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è, –∫–æ—Ç–æ—Ä–∞—è –∑–∞–ø—É—Å–∫–∞–µ—Ç –¥–∞—à–±–æ—Ä–¥ –∞–Ω–∞–ª–∏—Ç–∏–∫–∏
def run_analytics():
    # –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    st.title("üìä –ê–Ω–∞–ª–∏–∑ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–Ω–æ–≥–æ —Å–æ—Å—Ç–∞–≤–∞")

    # –ü–æ–¥–∑–∞–≥–æ–ª–æ–≤–æ–∫ –∏ –≤—ã–±–æ—Ä —Ä–µ–∂–∏–º–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö
    st.markdown("### üîç –í—ã–±–µ—Ä–∏—Ç–µ –∏—Å—Ç–æ—á–Ω–∏–∫ –¥–∞–Ω–Ω—ã—Ö:")
    mode = st.radio("–†–µ–∂–∏–º –∞–Ω–∞–ª–∏–∑–∞:", ["–ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö", "–†—É—á–Ω–æ–π –≤–≤–æ–¥"])  # –í—ã–±–æ—Ä –º–µ–∂–¥—É –ë–î –∏ —Ä—É—á–Ω—ã–º –≤–≤–æ–¥–æ–º

    # –ï—Å–ª–∏ –≤—ã–±—Ä–∞–Ω —Ä–µ–∂–∏–º "–ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö":
    if mode == "–ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö":
        # –ó–∞–≥—Ä—É–∂–∞–µ–º Excel-—Ñ–∞–π–ª —Å –¥–∞–Ω–Ω—ã–º–∏ (–ø—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ—Ç—Å—è, —á—Ç–æ –æ–Ω –ª–µ–∂–∏—Ç —Ä—è–¥–æ–º)
        df = pd.read_excel("grid.xlsx")

        # –°–ª–æ–≤–∞—Ä—å –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö —Ñ–∏–ª—å—Ç—Ä–æ–≤ (–º–µ—Å—Ç–æ—Ä–æ–∂–¥–µ–Ω–∏–µ, –î–ù–°, —Å—Ç—É–ø–µ–Ω—å)
        fields = {}
        if "–ú–µ—Å—Ç–æ—Ä–æ–∂–¥–µ–Ω–∏–µ" in df.columns:
            # –ï—Å–ª–∏ –∫–æ–ª–æ–Ω–∫–∞ –µ—Å—Ç—å, –æ—Ç–æ–±—Ä–∞–∂–∞–µ–º –º—É–ª—å—Ç–∏–≤—ã–±–æ—Ä –º–µ—Å—Ç–æ—Ä–æ–∂–¥–µ–Ω–∏–π
            fields['–ú–µ—Å—Ç–æ—Ä–æ–∂–¥–µ–Ω–∏–µ'] = st.multiselect("–í—ã–±–µ—Ä–∏—Ç–µ –º–µ—Å—Ç–æ—Ä–æ–∂–¥–µ–Ω–∏–µ:", df['–ú–µ—Å—Ç–æ—Ä–æ–∂–¥–µ–Ω–∏–µ'].unique())
        if "–î–ù–°" in df.columns:
            # –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ –¥–ª—è –î–ù–°
            fields['–î–ù–°'] = st.multiselect("–í—ã–±–µ—Ä–∏—Ç–µ –î–ù–°:", df['–î–ù–°'].unique())
        if "–°—Ç—É–ø–µ–Ω—å –æ—Ç–±–æ—Ä–∞" in df.columns:
            # –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ –¥–ª—è —Å—Ç—É–ø–µ–Ω–∏ –æ—Ç–±–æ—Ä–∞
            fields['–°—Ç—É–ø–µ–Ω—å –æ—Ç–±–æ—Ä–∞'] = st.multiselect("–í—ã–±–µ—Ä–∏—Ç–µ —Å—Ç—É–ø–µ–Ω—å –æ—Ç–±–æ—Ä–∞:", df['–°—Ç—É–ø–µ–Ω—å –æ—Ç–±–æ—Ä–∞'].unique())

        # –°–æ–∑–¥–∞–µ–º –∫–æ–ø–∏—é –∏—Å—Ö–æ–¥–Ω–æ–≥–æ –¥–∞—Ç–∞—Ñ—Ä–µ–π–º–∞ –¥–ª—è —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏
        filtered_df = df.copy()

        # –ü—Ä–∏–º–µ–Ω—è–µ–º —Ñ–∏–ª—å—Ç—Ä—ã –ø–æ –≤—ã–±—Ä–∞–Ω–Ω—ã–º –∑–Ω–∞—á–µ–Ω–∏—è–º
        for key, values in fields.items():
            if values:
                # –û—Å—Ç–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ —Ç–µ —Å—Ç—Ä–æ–∫–∏, –≥–¥–µ –∑–Ω–∞—á–µ–Ω–∏—è —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—Ç –≤—ã–±—Ä–∞–Ω–Ω—ã–º
                filtered_df = filtered_df[filtered_df[key].isin(values)]

        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –æ—Ç—Ñ–∏–ª—å—Ç—Ä–æ–≤–∞–Ω–Ω—ã–π –¥–∞—Ç–∞—Ñ—Ä–µ–π–º –≤ —Å–µ—Å—Å–∏—é, —á—Ç–æ–±—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –≤ –¥—Ä—É–≥–∏—Ö –º–æ–¥—É–ª—è—Ö
        st.session_state["filtered_df"] = filtered_df

        # –ë–ª–æ–∫ —Å –≤—ã–±–æ—Ä–æ–º —Ç–∏–ø–∞ –∞–Ω–∞–ª–∏–∑–∞: –°–ù‚ÇÑ, –°‚ÇÉ+–≤, –°‚ÇÖ+–≤
        st.write("## –í—ã–±–µ—Ä–∏—Ç–µ —Ç–∏–ø –∞–Ω–∞–ª–∏–∑–∞:")
        col1, col2, col3 = st.columns(3)  # –†–∞–∑–±–∏–≤–∞–µ–º –Ω–∞ 3 –∫–æ–ª–æ–Ω–∫–∏

        # –ö–Ω–æ–ø–∫–∞ –∑–∞–ø—É—Å–∫–∞ –∞–Ω–∞–ª–∏–∑–∞ –°–ù‚ÇÑ
        with col1:
            if st.button("–ê–Ω–∞–ª–∏–∑ –°–ù‚ÇÑ"):
                run_ch4()  # –∑–∞–ø—É—Å–∫–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–π –º–æ–¥—É–ª—å

        # –ö–Ω–æ–ø–∫–∞ –∑–∞–ø—É—Å–∫–∞ –∞–Ω–∞–ª–∏–∑–∞ –°‚ÇÉ+–≤
        with col2:
            if st.button("–ê–Ω–∞–ª–∏–∑ –°‚ÇÉ+–≤"):
                run_c3()

        # –ö–Ω–æ–ø–∫–∞ –∑–∞–ø—É—Å–∫–∞ –∞–Ω–∞–ª–∏–∑–∞ –°‚ÇÖ+–≤
        with col3:
            if st.button("–ê–Ω–∞–ª–∏–∑ –°‚ÇÖ+–≤"):
                run_c5()

    # –ï—Å–ª–∏ –≤—ã–±—Ä–∞–Ω —Ä–µ–∂–∏–º "–†—É—á–Ω–æ–π –≤–≤–æ–¥":
    elif mode == "–†—É—á–Ω–æ–π –≤–≤–æ–¥":
        # –ü–æ–∫–∞ —ç—Ç–æ—Ç —Ä–µ–∂–∏–º –Ω–µ —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω
        st.info("üìù –†–µ–∂–∏–º —Ä—É—á–Ω–æ–≥–æ –≤–≤–æ–¥–∞ –ø–æ–∫–∞ –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–µ.")
